public interface Animal {
    // mọi phương thức trong interface đều có phương thức public và abstract (mặc định dều có public và abstract).

    void move(); // phương thức mô tả cách thức di chuyển của loài động vật.
    void eat(); // phương thức mô tả cách thức ăn uống của loài động vật.
    void sleep(); // phương thức mô tả cách thức ngủ của loài động vật.

    // chỉ ra hành động chung nhất của lớp nào đó.













}
